#include "ft_printf.h"

void	ft_puttstr(char *s, int *ptr)
{
	int	i;

	i = 0;
	if (!s)
		*ptr += write(1, "(null)", 6);
	else
	{
		while (s[i])
		{
			*ptr += write(1, &s[i], 1);
			i++;
		}
	}
}
